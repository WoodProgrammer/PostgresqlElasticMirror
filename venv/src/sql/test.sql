CREATE OR REPLACE FUNCTION notify_CHANNEL_NAME()
  RETURNS trigger AS $$
DECLARE
BEGIN
  PERFORM pg_notify(
    CAST('notify_CHANNEL_NAME' AS text),
    row_to_json(NEW)::text);
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;


CREATE TRIGGER notify_CHANNEL_NAME
  AFTER INSERT ON TABLE_NAME
  FOR EACH ROW
  EXECUTE PROCEDURE notify_CHANNEL_NAME();
